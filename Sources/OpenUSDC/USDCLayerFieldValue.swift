public enum USDCLayerFieldValue: Sendable, Equatable {
    case token(String)
    case tokenArray([String])
    case tokenVector([String])
    case string(String)
    case assetPath(String)
    case pathVector([String])
    case tokenListOp(USDCListOp<String>)
    case stringListOp(USDCListOp<String>)
    case pathListOp(USDCListOp<String>)
    case referenceListOp(USDCListOp<USDCReference>)
    case payloadListOp(USDCListOp<USDCPayload>)
    case payload(USDCPayload)
    case double(Double)
    case intArray([Int])
    case specifier(USDCPrimSpecifier)
}
